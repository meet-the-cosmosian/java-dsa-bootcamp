package meet_java_fundamentals.L02_variables_datatypes;
// Try to declare meaningful variables of each type. Eg - a variable named age should be a numeric type (int or float) not byte.
public class Q1_MeaningfulVariables {
    public static void main(String[] args) {
        String name  = "John";
        int age = 21;
        float height = 6.1f; // in feet

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height + " feet");
    }   
}
